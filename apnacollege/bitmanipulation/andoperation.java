package bitmanipulation;

public class andoperation {
    
    public static void main(String[] args) {
        int a =5,b=6;
        System.out.println(a|b);
        System.out.println(a&b);
        System.out.println(a^b);
        System.out.println(~a);
        System.out.println(5<<2);//  a<<b=a*2^b
        System.out.println(5>>2);//  a<<b=a/2^b
    }
}
